# عدد n رو بگیره
# این شکل رو بکشه :

# * 1
# * *
# * * *
# * * * *
# * * * * *
# * * * * * *
# * * * * * * * => n
# * * * * * * => n-1
# * * * * *
# * * * *
# * * *
# * *
# * => 1

n = int(input())
for i in range(1, n + 1):
    print(i * "%", "'")
for i in range(n - 1, 0, -1):
    print(i * "%", "'")
